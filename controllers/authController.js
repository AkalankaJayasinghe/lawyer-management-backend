const User = require('../models/userModel');
const Lawyer = require('../models/lawyerModel');
const jwt = require('jsonwebtoken');
const bcrypt = require('bcryptjs');
const sendEmail = require('../utils/emailService');

// @desc    Register user
// @route   POST /api/auth/register
// @access  Public
exports.register = async (req, res, next) => {
  try {
    const { name, email, password, role } = req.body;

    // Check if user already exists
    let user = await User.findOne({ email });

    if (user) {
      return res.status(400).json({
        success: false,
        message: 'User already exists',
      });
    }

    // Create user
    user = await User.create({
      name,
      email,
      password,
      role,
    });

    // If registering as a lawyer, create lawyer profile
    if (role === 'lawyer') {
      const { specialization, bio, experience, hourlyRate } = req.body;
      
      // Validate required lawyer fields
      if (!specialization || !bio || !experience || !hourlyRate) {
        await User.findByIdAndDelete(user._id);
        return res.status(400).json({
          success: false,
          message: 'Please provide all required lawyer information',
        });
      }

      await Lawyer.create({
        user: user._id,
        specialization,
        bio,
        experience,
        hourlyRate,
      });
    }

    // Send welcome email
    try {
      await sendEmail({
        email: user.email,
        subject: 'Welcome to Lawyer Management System',
        message: `Dear ${user.name}, thank you for registering with our platform.`,
      });
    } catch (err) {
      console.log('Email could not be sent', err);
    }

    sendTokenResponse(user, 201, res);
  } catch (err) {
    res.status(500).json({
      success: false,
      message: err.message,
    });
  }
};

// @desc    Login user
// @route   POST /api/auth/login
// @access  Public
exports.login = async (req, res, next) => {
  try {
    const { email, password } = req.body;

    // Validate email & password
    if (!email || !password) {
      return res.status(400).json({
        success: false,
        message: 'Please provide email and password',
      });
    }

    // Check for user
    const user = await User.findOne({ email }).select('+password');

    if (!user) {
      return res.status(401).json({
        success: false,
        message: 'Invalid credentials',
      });
    }

    // Check if password matches
    const isMatch = await user.matchPassword(password);

    if (!isMatch) {
      return res.status(401).json({
        success: false,
        message: 'Invalid credentials',
      });
    }

    sendTokenResponse(user, 200, res);
  } catch (err) {
    res.status(500).json({
      success: false,
      message: err.message,
    });
  }
};

